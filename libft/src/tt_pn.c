#include "libft.h"

int	tt_pn(int n)
{
	if (n == -2147483648)
		tt_ps("-2147483648");
	else
	{
		if (n < 0)
		{
			n = -n;
			tt_pc('-');
		}
		if (n >= 10)
			tt_pn(n / 10);
		tt_pc(n % 10 + 48);
	}
	return (0);
}

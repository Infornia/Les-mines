#include "libft.h"

char	*ft_strncpy(char *dst, char *src, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n && src[i])
	{
		dst[i] = src[i];
		i++;
	}
	if (i < n)
		while (i < n)
		{
			dst[i] = '\0';
			i++;
		}
	return (dst);
}
